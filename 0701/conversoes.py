def celcius_farenheit(C: float) -> float:
    """Converte temperatura em Celsius para Fahrenheit.
    
    Fórmula: F = C * 9 / 5 + 32
    """
    return C * 9 / 5 + 32


def metros_quilometros(m: float) -> float:
    """Converte distância em metros para quilômetros.
    
    1 km = 1000 m
    """
    return m / 1000